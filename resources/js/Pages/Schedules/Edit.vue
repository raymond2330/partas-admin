<script setup>
import AuthenticatedLayout from '@/Layouts/AuthenticatedLayout.vue';
import { Head,router  } from '@inertiajs/vue3';
import PrimaryButton from '@/Components/PrimaryButton.vue'
import InputLabel from '@/Components/InputLabel.vue'
import TextInput from '@/Components/TextInput.vue'
import { reactive,ref } from 'vue'

const props = defineProps({
    busSchedule: Object,
    routes: Object,
    buses: Object

  })

  const form= reactive({
            arrival_time: props.busSchedule.arrival_time,
            departure_time: props.busSchedule.departure_time,
            bus_id: props.busSchedule.bus_id,
            route_id:props.busSchedule.route_id,
            price:props.busSchedule.price
    })

    function update() {
        router.post(`/schedule/${props.busSchedule.id}`, {
            _method: 'put',
            arrival_time: form.arrival_time,
            departure_time: form.departure_time,
            bus_id:  form.bus_id,
            route_id: form.route_id,
            price: form.price
        })
  }

</script>



<template>
    <Head title="Update Bus Schedule" />

    <AuthenticatedLayout>
        

        <div class="py-12">
            <div class="max-w-7xl mx-auto sm:px-6 lg:px-8">
                <div class="overflow-hidden ma-8 w-100  bg-[#919abc] rounded-lg p-5 shadow-xs">
                    <div class="overflow-x-auto  mx-8 ">
                            <form @submit.prevent="update" >
                                <div class="md:w-1/3">
                                <InputLabel for="departure_time" class="" value="Departure Time"/>
                                </div>
                                <div class="block w-full text-gray-900">

                                    <TextInput  id="departure_time" type="datetime-local" v-model="form.departure_time" required />
                                </div>
                                <div>
                                    <InputLabel for="arrival_time"  value="Arrival Time"/>
                                </div>
                                <div class="block w-full text-gray-900">
                                    <TextInput  id="arrival_time"  type="datetime-local" v-model="form.arrival_time" required />
                                </div>
                                <div>
                                    <InputLabel for="price"  value="Price"/>
                                </div>
                                <div class="block w-full text-gray-900">
                                    <TextInput  id="price"  type="number" v-model="form.price" required />
                                </div>

                                <InputLabel for="route" class="block font-medium text-gray-700">Select Bus Route:</InputLabel>
                                <select id="route" v-model="form.route_id" class="mt-1 block w-full px-4 py-2 bg-white border border-gray-300 rounded-md shadow-sm text-gray-900 focus:ring focus:ring-white focus:outline-none focus:border-white">
                                    <option v-for="route in routes"  :value="route.id">{{ route.origin }} - {{ route.destination }}</option>
                                 </select>

                                 <InputLabel for="bus" class="block font-medium text-gray-700">Select Bus </InputLabel>
                                <select id="route" v-model="form.bus_id" class="mt-1 block w-full px-4 py-2 bg-white border border-gray-300 rounded-md shadow-sm  text-gray-900 focus:ring focus:ring-white focus:outline-none focus:border-white">
                                    <option v-for="bus in buses"  :value="bus.id">{{ bus.code }} - {{ bus.type }}</option>
                                 </select>

                                <div class=" py-3 md:w-1/3">
                                <PrimaryButton type="submit"> Submit  </PrimaryButton>
                                </div>

                            </form>
                    </div>
                </div>
            </div>
        </div>

    </AuthenticatedLayout>
</template>
